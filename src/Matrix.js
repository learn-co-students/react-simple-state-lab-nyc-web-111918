import React, { Component } from 'react';
import UUID from 'uuid'
import Cell from './Cell'

export default class Matrix extends Component {
  constructor(props) {
    super(props)
  }

  genRow = (vals) => (
    vals.map(val => <Cell key={UUID()} value={val}/>) // replace me and render a cell component instead!
  )

  genMatrix = () => (
    this.props.values.map(rowVals => <div className="row" key={UUID()}>{this.genRow(rowVals)}</div>)
  )

  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }

}

